    public void populateStreetComboBox() {
        streetJComboBox.removeAllItems();
        for (Street street : enterprise.getStreetDirectory().getStreetList()) {
            streetJComboBox.addItem(street);
        }
    }

    public void populateSensorComboBox(Street street) {
        sensorJComboBox.removeAllItems();
        for (Sensor sensor : street.getSensorDirectory().getSensorList()) {
            sensorJComboBox.addItem(sensor);
        }
    }

    public void populateAirIndexTable() {
        DefaultTableModel model = (DefaultTableModel) airIndexJTable.getModel();
        model.setRowCount(0);
        //Street street = (Street) streetJComboBox.getSelectedItem();
        Sensor sensor = (Sensor) sensorJComboBox.getSelectedItem();
        for (AirIndex airIndex : sensor.getAirIndexDirectory().getAirIndexList()) {
            Object[] row = new Object[3];
            row[0] = airIndex.getAirIndex();
            row[1] = airIndex.getTimeStamp();
            row[2] = airIndex.getResult();
            model.addRow(row);
        }
    }

