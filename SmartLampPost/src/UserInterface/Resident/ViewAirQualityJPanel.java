    private void getAirIndexJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getAirIndexJButtonActionPerformed
        Street street = (Street) streetJComboBox.getSelectedItem();
        if (street.getSensorDirectory().getSensorList().size() == 0) {
            JOptionPane.showMessageDialog(null, "Street's Air Index currently unavailable.", "Sorry", JOptionPane.INFORMATION_MESSAGE);
        } else {
            populateAirIndexTable();
        }
    }//GEN-LAST:event_getAirIndexJButtonActionPerformed

    private void streetJComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_streetJComboBoxActionPerformed
        Street street = (Street) streetJComboBox.getSelectedItem();
        if (street != null) {
            populateSensorComboBox(street);
        }
    }//GEN-LAST:event_streetJComboBoxActionPerformed


