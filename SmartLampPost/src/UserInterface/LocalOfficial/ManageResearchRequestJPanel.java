    public void populateRequestDataTable() {
        DefaultTableModel model = (DefaultTableModel) researchRequestJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest request : localOfficialOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ResearchAirQualityDataRequest) {
                Object[] row = new Object[8];
                row[0] = request;
                row[1] = request.getSender();
                row[2] = request.getReceiver();
                row[3] = request.getRequestDate();
                row[4] = request.getResolveDate();
                row[5] = request.getMessage();
                row[6] = request.getStatus();
                row[7] = ((ResearchAirQualityDataRequest) request).getPermission();
                model.addRow(row);
            }
        }
    }

