    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        int selectedRow = researchRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            ResearchAirQualityDataRequest request = (ResearchAirQualityDataRequest) researchRequestJTable.getValueAt(selectedRow, 0);
            if (request.getStatus().equalsIgnoreCase("Pending")) {
                Date date = new Date();
                request.setResolveDate(date);
                request.setStatus("Completed");
                request.setPermission("Accepted");
                populateRequestDataTable();
            } else {
                JOptionPane.showMessageDialog(null, "Request has been already completed or unassigned.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_processJButtonActionPerformed

    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed
        int selectedRow = researchRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            WorkRequest request = (WorkRequest) researchRequestJTable.getValueAt(selectedRow, 0);
            if (request.getReceiver() == null) {
                request.setReceiver(userAccount);
                request.setStatus("Pending");
                populateRequestDataTable();
            } else {
                JOptionPane.showMessageDialog(null, "Request has been already assigned.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_assignJButtonActionPerformed


