    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        if (!Validation.isEmpty(nameJTextField) || !Validation.isEmpty(phoneNumberJTextField)) {
            JOptionPane.showMessageDialog(null, "Highlighted fields are mandatory. Please fill them.", "Success", JOptionPane.ERROR_MESSAGE);
        } else {
            Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
            String name = nameJTextField.getText();
            String a = "+1";
            String b = phoneNumberJTextField.getText();
            String phoneNumber = a + phoneNumberJTextField.getText();
            organization.getEmployeeDirectory().createEmployee(name, phoneNumber);
            JOptionPane.showMessageDialog(null, "Employee has been created.", "Success", JOptionPane.INFORMATION_MESSAGE);
            populateTable(organization);
            nameJTextField.setText("");
            phoneNumberJTextField.setText("");
            organizationJComboBox.setSelectedItem(organizationEmpJComboBox.getSelectedItem());
        }
    }//GEN-LAST:event_addJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

