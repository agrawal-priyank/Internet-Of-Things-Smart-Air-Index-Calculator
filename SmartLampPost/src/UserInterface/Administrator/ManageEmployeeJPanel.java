    public void populateOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Organization organization : organizationDir.getOrganizationList()) {
            organizationJComboBox.addItem(organization);
        }
    }

    public void populateOrganizationEmpComboBox() {
        organizationEmpJComboBox.removeAllItems();

        for (Organization organization : organizationDir.getOrganizationList()) {
            if ((organization instanceof EngineerOrganization) || (organization instanceof LocalOfficialOrganization)
                    || (organization instanceof ResearchOrganization)) {
                organizationEmpJComboBox.addItem(organization);
            }
        }
    }

    private void populateTable(Organization organization) {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        if (organization instanceof ResidentOrganization) {
            for (Resident employee : organization.getResidentDirectory().getResidentList()) {
                Object[] row = new Object[2];
                row[0] = employee.getFirstName();
                model.addRow(row);
            }
        } else {
            for (Employee employee : organization.getEmployeeDirectory().getEmployeeDirectory()) {
                Object[] row = new Object[2];
                row[0] = employee.getEmployeeName();
                row[1] = employee.getPhoneNumber();
                model.addRow(row);
            }

        }
    }

